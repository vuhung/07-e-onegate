package com.vportal.portlet.vlegal.util;

import javax.portlet.ActionRequest;
import javax.portlet.RenderRequest;
import javax.servlet.http.HttpServletRequest;

import com.liferay.portal.util.PortalUtil;

public class PortletUtilExt {

	public static long getPortletGroupId(RenderRequest request)throws Exception
	{
		return PortalUtil.getScopeGroupId(request);
	}
	
	public static long getPortletGroupId(ActionRequest request)throws Exception
	{
		return PortalUtil.getScopeGroupId(request);
	}
	
	public static long getPortletGroupId(HttpServletRequest request)throws Exception
	{
		return PortalUtil.getScopeGroupId(request);
	}
	
}
